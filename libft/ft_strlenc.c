#include "libft.h"

int		ft_strlenc(char const *s, int *i, char c)
{
	int	j;

	j = 0;
	while (s[*i] != '\0' && s[*i] != c)
	{
		j++;
		*i += 1;
	}
	if (s[j] == c)
		return (j);
	else
		return (j);
}
