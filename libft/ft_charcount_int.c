#include "libft.h"

int		ft_charcount_int(int k)
{
	int i;

	i = 1;
	if (k == -2147483648)
	{
		k = k + 2000000000;
		k = k * -1;
		i = i + 2;
	}
	if (k < 0)
	{
		k = k * -1;
		i++;
	}
	while (k > 9)
	{
		i++;
		k = k / 10;
	}
	return (i);
}
