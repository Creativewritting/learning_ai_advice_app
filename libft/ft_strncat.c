#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	i;
	char	*s3;

	i = 0;
	s3 = ft_strchr(s1, '\0');
	while (s2[i] != '\0' && i < n)
	{
		s3[i] = (char)s2[i];
		i++;
	}
	s3[i] = '\0';
	return (s1);
}
